# Temperature Converter (Float Casting Practice)
# Write a program that asks the user for a temperature in Celsius (which can contain a decimal, like 36.5). Convert it to Fahrenheit using the formula: Fahrenheit = (Celsius * 9/5) + 32, and print the result.

# Concept: float(input()) type casting.

Celsius = float(input("Enter temperature in Celsius : "))

Fahrenheit = (Celsius * 9/5) + 32

print(f" The temperature in Fahrenheit is {Fahrenheit}")